int ConvertUTF_Ex(unsigned char * pData, int nSize, int nLen, int nDestCodePage, int skipInvalid)
{
    unsigned char *pSrc  = pData;
    unsigned char *pDest = pData;
    unsigned char csrc;
    unsigned wc;

    // It is invalid to set skipInvalid mode while buffer size is not given
    if ( nSize < 0 && skipInvalid != 0 )
        return 0;
    
    if ( nLen  < 0 ) nLen  = INT_MAX;
    if ( nSize < 0 ) nSize = INT_MAX;
        
    if( !pData || *pData == 0 )
        return 0;
    if(nDestCodePage==CP_HIER_UTF){   //just count characters (and BOM too), not modify memory (pData) at all
        while( (nSize > 0) && (nLen-- > 0) ){
            csrc = *pData;
            if ( csrc == 0 )
            {
                if ( skipInvalid == 0 )
                    break;	// end of the data
                csrc = 0x20;
            }
                
            if((csrc & 0x80) == 0) {
                //wc = csrc;
                pData++;
                nSize--;
            }
            else if ( (csrc & 0xE0) == 0xC0 ) {
                if(nSize < 2 || (pData[1] & 0xC0) != 0x80)
                {
                    // wrong sequence
                    if ( skipInvalid == 0 )
                        break;
                    wc = 0x20; pData++; nSize--;
                }
                else
                {
                    wc = (((unsigned)(csrc & 0x1F)) << 6) | (pData[1] & 0x3F);
                    pData +=2;
                    nSize -=2;
                }
            }
            else if ( (csrc & 0xF0) == 0xE0 ) {
                if(nSize < 2 || (pData[1] & 0xC0) != 0x80)
                {
                    // wrong sequence
                    if ( skipInvalid == 0 )
                        break;
                    wc = 0x20; pData++; nSize--;
                }
                else if(nSize < 3 || (pData[2] & 0xC0) != 0x80)
                {
                    // wrong sequence
                    if ( skipInvalid == 0 )
                        break;	
                    wc = 0x20; pData++; nSize--;
                }
                else
                {
                    wc = ((((unsigned)(csrc & 0xF)) << 12)   | 
                          (((unsigned)pData[1] & 0x3F) << 6) |
                          (pData[2] & 0x3F));
                    pData +=3;
                    nSize -=3;
                }
            }
            else
            {
                if ( skipInvalid == 0 )
                    break;	// wrong sequence
                wc = 0x20; pData++; nSize--;
            }
        }
        //*pDest = 0;
        return int(pData-pSrc);
    }

    if(nSize>=3)
        if(*pData==0xEF && pData[1]==0xBB && pData[2]==0xBF){
            pData+=3;
            nSize-=3;
            nLen--;
        }

    while( (nSize > 0) && (nLen > 0) )
    {
        csrc = *pData;
        if ( csrc == 0 )
        {
            if ( skipInvalid == 0 )
                break;	// end of the data
            csrc = 0x20;
        }
            
        if((csrc & 0x80) == 0) {
            wc = csrc;
            pData++;
            nSize--;
        }
        else if ( (csrc & 0xE0) == 0xC0 ) {
            if(nSize < 2 || (pData[1] & 0xC0) != 0x80)
            {
                // wrong sequence
                if ( skipInvalid == 0 )
                    break;
                wc = 0x20; pData++; nSize--;
            }
            else
            {
                wc = (((unsigned)(csrc & 0x1F)) << 6) | (pData[1] & 0x3F);
                pData +=2;
                nSize -=2;
            }
        }
        else if ( (csrc & 0xF0) == 0xE0 ) {
            if(nSize < 2 || (pData[1] & 0xC0) != 0x80)
            {
                // wrong sequence
                if ( skipInvalid == 0 )
                    break;
                wc = 0x20; pData++; nSize--;
            }
            else if(nSize < 3 || (pData[2] & 0xC0) != 0x80)
            {
                // wrong sequence
                if ( skipInvalid == 0 )
                    break;	
                wc = 0x20; pData++; nSize--;
            }
            else
            {
                wc = ((((unsigned)(csrc & 0xF)) << 12)   | 
                      (((unsigned)pData[1] & 0x3F) << 6) |
                      (pData[2] & 0x3F));
                pData +=3;
                nSize -=3;
                if(wc==0xFEFF)  //BOM in unsigned representation, we ignore BOM
                    continue;
            }
        }
        else
        {
            if ( skipInvalid == 0 )
                break;	// wrong sequence
            wc = 0x20; pData++; nSize--;
        }
        //for Vietnamese codepage
        int nConv = Unicode2cpEx(wc,nDestCodePage,pDest,nLen);
        pDest += nConv;
        nLen -= nConv;
    }
    *pDest = 0;
    return int(pDest-pSrc);
}

