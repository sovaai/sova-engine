int ConvertCodepage(const unsigned char * pSrc, int ccSrc, int nSrcCodePage, unsigned char * pDst, int ccDst, int nDstCodePage){
//returns number of CHARS copied, not bytes
    if ( nDstCodePage   == CP_UTF8 )
        return Convert2UTF(pSrc,ccSrc, nSrcCodePage, pDst, ccDst);
    if ( nSrcCodePage == CP_UTF8 ) { 
        return ConvertUTF_Ex_2buf(pSrc,ccSrc,pDst,ccDst,nDstCodePage);
    }
    const unsigned char* pTab = CP2CP_Rus(nSrcCodePage,nDstCodePage);
    int nLenStr = ccSrc<0 ? int(strlen((char *)pSrc)) : ccSrc;
    nLenStr = nLenStr>ccDst ? ccDst : nLenStr;
    unsigned char *pEnd = pDst + nLenStr;
    if(nSrcCodePage==CP_HIER_UTF || nDstCodePage==CP_HIER_UTF){  //not convert, just copy and count characters
        while ( pDst < pEnd ) {
            *pDst = *pSrc; 
            pDst++;
            pSrc++;
        }
        return nLenStr;
    }
    while ( pDst < pEnd ) {
        *pDst = pTab[*pSrc]; 
        pDst++;
        pSrc++;
    }
    return nLenStr;
}

