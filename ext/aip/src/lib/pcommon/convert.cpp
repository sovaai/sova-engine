unsigned char Unicode2cp(unsigned wc, int nDestCodePage)
{
    unsigned char ch;
    Unicode2cpEx(wc, nDestCodePage, &ch, 1);
    return ch;
}

int Unicode2cpEx(unsigned wc, int nDestCodePage, unsigned char *pDst, int nDst)
{
    typedef const char * const TTable[256];
    TTable *pTab;

    if ( nDst <= 0 )
        return 0;
                
    uint8_t h = (wc >> 8) & 0xFF;
    uint8_t l = wc & 0xFF;
        
    switch(nDestCodePage)
    {
    case CP_KOI8R:
        pTab = &tabUsc2Koi;
        break;
    case CP_ISO8859_5:
        pTab = &tabUsc2Iso;
        break;
    case CP_OEM_RUSSIAN:
        pTab = &tabUsc2Dos;
        break;
    case CP_CYRILLIC_MAC:
        pTab = &tabUsc2Mac;
        break;
    case CP_CYRILLIC:
        pTab = &tabUsc2Win;
        break;
    case CP_LATIN1:
        pTab = &tabUsc2Lat1_cyr;
        break;
    case CP_VIETNAMESE:
        pTab = &tabUcs2Viet;

        if ( tabUcsVecViet[h]    != NULL &&
             tabUcsVecViet[h][l] != NULL )
        {
            const ucs_vec_t *vec = tabUcsVecViet[h][l];
            int total = 0;
            for ( size_t i = 0; i < vec->length; i++ )
                total += Unicode2cpEx( vec->vec[i], CP_VIETNAMESE, pDst + total, nDst - total );
            return total;
        }
                        
        break;
    default:
        *pDst = '?';
        return 1;
    }
    *pDst = (*pTab)[h][l];
    return 1;
}

int Cp2cpViaUnicode(unsigned char wc, int nCodePage, unsigned char *pDst, int nDst)
{
    switch (nCodePage)
    {
    case CP_LATIN1:       return Unicode2cpEx( tabLat12Usc[wc], nCodePage, pDst, nDst );
    case CP_CYRILLIC:     return Unicode2cpEx( tabWin2Usc[wc],  nCodePage, pDst, nDst );
    case CP_KOI8R:        return Unicode2cpEx( tabKoi2Usc[wc],  nCodePage, pDst, nDst );
    case CP_ISO8859_5:    return Unicode2cpEx( tabIso2Usc[wc],  nCodePage, pDst, nDst );
    case CP_OEM_RUSSIAN:  return Unicode2cpEx( tabDos2Usc[wc],  nCodePage, pDst, nDst );
    case CP_CYRILLIC_MAC: return Unicode2cpEx( tabMac2Usc[wc],  nCodePage, pDst, nDst );
    case CP_VIETNAMESE:   return Unicode2cpEx( tabViet2Ucs[wc], nCodePage, pDst, nDst );
    };

    return 0;
}

